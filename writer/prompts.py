def build_write_article_prompt(articles_formatted: str) -> str:
    """返回解读文章的 prompt，随机选取结构和角度。"""
    formats = [
        # 格式 A：深度分析
        """## 推荐格式：深度分析型

文章走向建议：
- 开头：用一个具体的行业变化、数据或事件制造悬念
- 展开：拆解这个变化背后的技术/商业逻辑
- 深化：和过去对比，说明为什么这次不一样
- 收束：给出判断——这意味着什么，接下来会怎样

风格：分析性、冷静、有判断力""",

        # 格式 B：故事驱动
        """## 推荐格式：故事驱动型

文章走向建议：
- 开头：从一个具体的人、场景或故事切入
- 展开：从这个故事引出更大的行业图景
- 深化：故事背后的技术/商业本质是什么
- 收束：回到开头故事，给出新的理解

风格：有叙事感、引人入胜、观点自然带出""",

        # 格式 C：问题导向
        """## 推荐格式：问题导向型

文章走向建议：
- 开头：提出一个核心问题（「为什么X突然火了？」「Y到底意味着什么？」）
- 展开：给出多个角度的回答，层层递进
- 深化：揭示最反直觉的那个答案
- 收束：把问题升维，留给读者思考空间

风格：有锐度、有洞察、启发思考""",

        # 格式 D：对比碰撞
        """## 推荐格式：对比碰撞型

文章走向建议：
- 开头：摆出两个看似矛盾的现象或判断
- 展开：分别展开各自的事实依据
- 深化：揭示两者背后的统一逻辑
- 收束：给出整合性的判断

风格：有张力、信息密度高、格局大""",
    ]

    chosen = random.choice(formats)

    return f"""请基于以下素材，创作一篇适合知乎发布的科技前沿解读文章。

每次写作的切入角度和结构都会变化，本次采用以下格式：

{chosen}

文章长度 1800-3000 字，不要强行写长。不要套模板感，让结构自然从素材生长出来。

素材：
{articles_formatted}

输出应包含以下章节（顺序不限）：
## 标题候选（3-5个）
## 推荐标题
## 摘要（100字以内）
## 正文
## 话题建议（3-5个）
## 配图建议（2-4个）
## 来源清单"""


def build_opportunity_prompt(articles_formatted: str) -> str:
    """返回机会拆解文章的 prompt，随机选取结构和角度。"""
    formats = [
        # 格式 A：机会地图
        """## 推荐格式：机会地图型

文章走向建议：
- 开头：从「这件事和你有关系」切入
- 展开：按不同人群/领域逐个拆解机会
- 深化：机会背后的共同逻辑是什么
- 收束：给出优先级排序和行动建议

风格：务实、直接、有可操作性""",

        # 格式 B：认知颠覆
        """## 推荐格式：认知颠覆型

文章走向建议：
- 开头：先抛出一个流行的共识，然后质疑它
- 展开：用事实说明这个共识为什么有问题
- 深化：给出新的判断框架
- 收束：在这个新框架下，真正的机会在哪

风格：有锐度、反共识、启发感""",

        # 格式 C：实战路线
        """## 推荐格式：实战路线型

文章走向建议：
- 开头：从「如果你今天想做，第一步是什么」开始
- 展开：按时间线或能力线拆解行动路径
- 深化：每条路径上的关键卡点和破解方法
- 收束：下一步具体动作

风格：干货导向、像朋友聊天、不端着""",

        # 格式 D：逆向思考
        """## 推荐格式：逆向思考型

文章走向建议：
- 开头：先列出大家都在追的热点
- 展开：逐条拆解哪些是噱头、哪些是真机会
- 深化：为什么真机会长这样，噱头为什么是噱头
- 收束：给出与众不同的判断

风格：冷静、有判断力、敢于说「不」""",
    ]

    chosen = random.choice(formats)

    return f"""请基于以下素材，创作一篇「前沿科技机会拆解」知乎文章。

每次写作的切入角度和结构都会变化，本次采用以下格式：

{chosen}

文章长度 1800-3000 字，不要强行写长。不要套模板感，让结构自然从素材生长出来。

素材：
{articles_formatted}

输出应包含以下章节（顺序不限）：
## 标题候选（3-5个）
## 推荐标题
## 摘要（100字以内）
## 正文
## 话题建议（3-5个）
## 配图建议（2-4个）
## 来源清单"""


